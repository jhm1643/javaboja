package api.domain;

public class MadnCommand {

	private String cm;
	private String tenant;
	private String madn;
	
	public String getCm() {
		return cm;
	}
	public void setCm(String cm) {
		this.cm = cm;
	}
	public String getTenant() {
		return tenant;
	}
	public void setTenant(String tenant) {
		this.tenant = tenant;
	}
	public String getMadn() {
		return madn;
	}
	public void setMadn(String madn) {
		this.madn = madn;
	}
}

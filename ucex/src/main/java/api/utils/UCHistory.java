package api.utils;

public class UCHistory {

	private int		history_id;
	private String	history_table;
	private String	history_key;
	private String	history_type;
	private String	history_process;
	private String	history_createdate;
	private String	history_updatedate;
	
	public static enum HISTORY_TYPE { INSERT, UPDATE, DELETE }
	
	public int getHistory_id() {
		return history_id;
	}
	public void setHistory_id(int history_id) {
		this.history_id = history_id;
	}
	public String getHistory_table() {
		return history_table;
	}
	public void setHistory_table(String history_table) {
		this.history_table = history_table;
	}
	public String getHistory_key() {
		return history_key;
	}
	public void setHistory_key(String history_key) {
		this.history_key = history_key;
	}
	public String getHistory_type() {
		return history_type;
	}
	public void setHistory_type(String history_type) {
		this.history_type = history_type;
	}
	public String getHistory_process() {
		return history_process;
	}
	public void setHistory_process(String history_process) {
		this.history_process = history_process;
	}
	public String getHistory_createdate() {
		return history_createdate;
	}
	public void setHistory_createdate(String history_createdate) {
		this.history_createdate = history_createdate;
	}
	public String getHistory_updatedate() {
		return history_updatedate;
	}
	public void setHistory_updatedate(String history_updatedate) {
		this.history_updatedate = history_updatedate;
	}
	
}
